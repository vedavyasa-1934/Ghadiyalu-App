def build_time(h, m, ap):
    h, m = int(h), int(m)
    if ap == "PM" and h != 12:
        h += 12
    if ap == "AM" and h == 12:
        h = 0
    return datetime.strptime(f"{h:02d}:{m:02d}", "%H:%M").time()


def seconds_per_ghadi(total_minutes):
    return int((total_minutes * 60) / 30)


def build_rows(start, seconds, batch, weekday):
    rows = []
    current = start

    for ghadi in range(1, 31):
        end = current + timedelta(seconds=seconds)
        rows.append({
            "‡∞µ‡∞æ‡∞∞‡∞Ç":TELUGU_WEEK[weekday],
            "‡∞¨‡±ç‡∞Ø‡∞æ‡∞ö‡±ç": batch,
            "‡∞ò‡∞°‡∞ø‡∞Ø": ghadi,
            "‡∞Æ‡±ä‡∞¶‡∞≤‡±Å": current.strftime("%I:%M:%S %p"),
            "‡∞µ‡∞∞‡∞ï‡±Ç": end.strftime("%I:%M:%S %p"),
            "‡∞´‡∞≤‡∞Ç": FALALU.get(weekday, {}).get(ghadi, "")
        })
        current = end

    return rows


def highlight_active(df, start_times, end_times):
    now = datetime.now(IST)

    def style(row):
        idx = row.name
        if start_times[idx] <= now <= end_times[idx]:
            return ["background-color:#1B5E20;color:white;font-weight:bold"] * len(row)
        return [""] * len(row)

    return df.style.apply(style, axis=1)

